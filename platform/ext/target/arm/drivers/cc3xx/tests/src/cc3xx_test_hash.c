uint8_t *output_from_alg_and_data(cc3xx_hash_alg_t alg,
                                  struct hash_test_data_t *data)
{
    switch (alg) {
        case CC3XX_HASH_ALG_SHA256:
            return data->output.sha256;
        case CC3XX_HASH_ALG_SHA224:
            return data->output.sha224;
        case CC3XX_HASH_ALG_SHA1:
            return data->output.sha1;
    }
}

size_t hash_size_from_alg(cc3xx_hash_alg_t alg)
{
    switch (alg) {
        case CC3XX_HASH_ALG_SHA256:
            return SHA256_OUTPUT_SIZE;
        case CC3XX_HASH_ALG_SHA224:
            return SHA224_OUTPUT_SIZE;
        case CC3XX_HASH_ALG_SHA1:
            return SHA1_OUTPUT_SIZE;
    }
}

#define CREATE_HASH_TESTSUITE(alg) \
static void hash_ ## alg  ## _lowlevel_tests_run(struct test_result_t *ret) \
{ \
    TEST_ASSERT(hash_test_lowlevel_oneshot(&hash_test_block, alg) == 0, \
                "hash_test_block should pass"); \
    TEST_ASSERT(hash_test_lowlevel_oneshot(&hash_test_short, alg) == 0, \
                "hash_test_short should pass"); \
    TEST_ASSERT(hash_test_lowlevel_oneshot(&hash_test_long, alg) == 0, \
                "hash_test_long should pass"); \
    TEST_ASSERT(hash_test_lowlevel_oneshot(&hash_test_zero, alg) == 0, \
                "hash_test_zero should pass"); \
    TEST_ASSERT(hash_test_lowlevel_multipart(&hash_test_long, alg, 32) == 0, \
                "multipart hash_test_long should pass with chunk size 32"); \
    TEST_ASSERT(hash_test_lowlevel_multipart(&hash_test_long, alg, 31) == 0, \
                "multipart hash_test_long should pass with chunk size 31"); \
    TEST_ASSERT(hash_test_lowlevel_multipart(&hash_test_block, alg, 16) == 0, \
                "multipart hash_test_block should pass with chunk size 16"); \
    TEST_ASSERT(hash_test_lowlevel_multipart(&hash_test_block, alg, 7) == 0, \
                "multipart hash_test_block should pass with chunk size 7"); \
    TEST_ASSERT(hash_test_lowlevel_multipart(&hash_test_long, alg, 64) == 0, \
                "multipart hash_test_long should pass with chunk size 64"); \
    TEST_ASSERT(hash_test_lowlevel_saveload_multipart(&hash_test_long, alg, 64) == 0, \
                "multipart saveload hash_test_long should pass with chunk size 64"); \
    TEST_ASSERT(hash_test_lowlevel_reinit(&hash_test_block, alg) == 0, \
                "reiniting hash_test_block should pass"); \
    ret->val = TEST_PASSED; \
    return; \
} \
static struct test_t hash_ ## alg ## _tests = { \
    &hash_ ## alg ## _lowlevel_tests_run, \
     # alg "_TEST", \
    "CC3XX Hash tests (" # alg ")" \
};

CREATE_HASH_TESTSUITE(CC3XX_HASH_ALG_SHA256);
CREATE_HASH_TESTSUITE(CC3XX_HASH_ALG_SHA224);
CREATE_HASH_TESTSUITE(CC3XX_HASH_ALG_SHA1);

void add_cc3xx_hash_tests_to_testsuite(struct test_suite_t *p_ts, uint32_t ts_size)
{
#ifdef CC3XX_CONFIG_HASH_SHA256_ENABLE
    cc3xx_add_tests_to_testsuite(&hash_CC3XX_HASH_ALG_SHA256_tests, 1, p_ts, ts_size);
#endif /* CC3XX_CONFIG_HASH_SHA256_ENABLE */

#ifdef CC3XX_CONFIG_HASH_SHA224_ENABLE
    cc3xx_add_tests_to_testsuite(&hash_CC3XX_HASH_ALG_SHA224_tests, 1, p_ts, ts_size);
#endif /* CC3XX_CONFIG_HASH_SHA224_ENABLE */

#ifdef CC3XX_CONFIG_HASH_SHA1_ENABLE
    cc3xx_add_tests_to_testsuite(&hash_CC3XX_HASH_ALG_SHA1_tests, 1, p_ts, ts_size);
#endif /* CC3XX_CONFIG_HASH_SHA256_ENABLE */
}
